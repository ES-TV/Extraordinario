#include <iostream>
using namespace std;

int main()
{
    int numero;
    cout << "Escriba un numero entre 1 y 5 \n";
    cin >> numero;
    switch (numero)
    {
    case 1:
        cout << "Caso 1\n";
        break;
    case 2:
        cout << "Caso 2\n";
        break;
    case 3:
        cout << "Caso 3\n";
        break;
    case 4:
        cout << "Caso 4\n";
        break;
    case 5:
        cout << "Caso 5\n";
        break;
    default:
         
        break;
    }
    return 0;
}

